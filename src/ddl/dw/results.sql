CREATE TABLE IF NOT EXISTS dw.results (
    prog_id NUMBER,
    event_id NUMBER,
    athlete_id NUMBER,
    prog_date DATE,
    finish_position VARCHAR(256),
    total_time VARCHAR(256),
    prog_distance_category VARCHAR(256),
    prog_distances VARIANT,
    prog_notes VARCHAR,
    seg1 VARCHAR(256),
    seg2 VARCHAR(256),
    seg3 VARCHAR(256),
    seg4 VARCHAR(256),
    seg5 VARCHAR(256),
    split1 VARCHAR(256),
    split2 VARCHAR(256),
    split3 VARCHAR(256),
    split4 VARCHAR(256),
    split5 VARCHAR(256),
    CONSTRAINT result_pk PRIMARY KEY (prog_id, athlete_id)
)